# listas, tuiplas e dicionários são estruturas de dados fundamentais em Python que permitem armazenar e manipular coleções de itens.
# Listas são coleções ordenadas e mutáveis, tuplas são coleções ordenadas e imutáveis, e dicionários são coleções de pares chave-valor que permitem acesso rápido aos itens.
# Essas estruturas são amplamente utilizadas em programação para organizar e gerenciar dados de forma eficiente.

# Exemplo de listas, tuplas e dicionários em Python
def listas_tuplas_dicionarios():
    # Listas: coleções ordenadas e mutáveis
    lista = [1, 2, 3, 4, 5]
    print("Lista:", lista)

    # Tuplas: coleções ordenadas e imutáveis
    tupla = (1, 2, 3, 4, 5)
    print("Tupla:", tupla)

    # Dicionários: coleções de pares chave-valor
    dicionario = {'chave1': 'valor1', 'chave2': 'valor2'}
    print("Dicionário:", dicionario)
    
# Chamada da função de listas, tuplas e dicionários
listas_tuplas_dicionarios()
