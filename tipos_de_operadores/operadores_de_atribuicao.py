# Operadores de atribuição em Python: =, +=, -=, *=, /=, //=, %=, **=

valor = 10

# Atribuição simples
valor_atribuido = valor
print("Atribuição simples:", valor_atribuido)

# Atribuição com adição
valor_atribuido += 5  # Equivalente a valor = valor + 5
print("Atribuição com adição:", valor_atribuido)

# Atribuição com subtração
valor_atribuido -= 3  # Equivalente a valor = valor - 3
print("Atribuição com subtração:", valor_atribuido)

# Atribuição com multiplicação
valor_atribuido *= 2  # Equivalente a valor = valor * 2
print("Atribuição com multiplicação:", valor_atribuido)

# Atribuição com divisão
valor_atribuido /= 4  # Equivalente a valor = valor / 4
print("Atribuição com divisão:", valor_atribuido)

# Atribuição com divisão inteira
valor_atribuido //= 2  # Equivalente a valor = valor // 2
print("Atribuição com divisão inteira:", valor_atribuido)

# Atribuição com módulo
valor_atribuido %= 3  # Equivalente a valor = valor % 3
print("Atribuição com módulo:", valor_atribuido)

# Atribuição com exponenciação
valor_atribuido **= 2  # Equivalente a valor = valor ** 2
print("Atribuição com exponenciação:", valor_atribuido)
