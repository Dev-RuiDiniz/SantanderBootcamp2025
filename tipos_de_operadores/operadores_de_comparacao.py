# Operadores de comparação em Python: ==, !=, <, >, <=, >=

valor_1 = 10
valor_2 = 5

# Igualdade
igualdade = valor_1 == valor_2
print("Igualdade:", igualdade)

# Desigualdade
desigualdade = valor_1 != valor_2
print("Desigualdade:", desigualdade)

# Menor que
menor_que = valor_1 < valor_2
print("Menor que:", menor_que)

# Maior que
maior_que = valor_1 > valor_2
print("Maior que:", maior_que)

# Menor ou igual a
menor_ou_igual = valor_1 <= valor_2
print("Menor ou igual a:", menor_ou_igual)

# Maior ou igual a
maior_ou_igual = valor_1 >= valor_2
print("Maior ou igual a:", maior_ou_igual)
